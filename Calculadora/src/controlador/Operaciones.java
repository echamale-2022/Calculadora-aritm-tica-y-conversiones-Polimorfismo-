public class Operaciones {
  public int suma(int num1,int num2){
      int total = 0;
      total = num1 + num2;
      return total;
  }
  public double suma(double num3,double num4){
      double total = 0;
      total = num3 + num4;
      return total;
  }
  public int resta(int num1,int num2){
      int total = 0;
      total = num1 - num2;
      return total;
  }
  public double resta(double num3,double num4){
      double total = 0;
      total = num3 - num4;
      return total;
  }
  public int multiplicacion(int num1,int num2){
      int total = 0;
      total = num1 * num2;
      return total;
  }
  public double multiplicacion(double num3,double num4){
      double total = 0;
      total = num3 * num4;
      return total;
  }
  public int divicion(int num1,int num2){  
    int total = 0;
    total = num1 / num2;
    return total;
  }
  public double divicion(double num3,double num4){
      double total = 0;
      total = num3 / num4;
      return total;
  }

}
